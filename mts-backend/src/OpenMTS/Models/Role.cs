namespace OpenMTS.Models
{
    /// <summary>
    /// Represents a user role.
    /// </summary>
    public enum Role
    {
        /// <summary>
        /// The administrator user role.
        /// </summary>
        Administrator = 0,
        /// <summary>
        /// The scientific assistant user role.
        /// </summary>
        ScientificAssistant = 1,

        /// <summary>
        /// The standard user role.
        /// </summary>
        User = 2
    }
}
